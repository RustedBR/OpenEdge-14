        private async void ImportProfile()
        {
            if (_exporting || CharacterSlot == null || Profile == null)
                return;

            StartExport();
            await using var file = await _dialogManager.OpenFile(new FileDialogFilters(new FileDialogFilters.Group("yml")));

            if (file == null)
            {
                EndExport();
                return;
            }

            try
            {
                var profile = _entManager.System<HumanoidAppearanceSystem>().FromStream(file, _playerManager.LocalSession!);
                var oldProfile = Profile;
                SetProfile(profile, CharacterSlot);

                IsDirty = !profile.MemberwiseEquals(oldProfile);
            }
            catch (Exception exc)
            {
                _sawmill.Error($"Error when importing profile\n{exc.StackTrace}");
            }
            finally
            {
                EndExport();
            }
        }

        private async void ExportProfile()
        {
            if (Profile == null || _exporting)
                return;

            StartExport();
            var file = await _dialogManager.SaveFile(new FileDialogFilters(new FileDialogFilters.Group("yml")));

            if (file == null)
            {
                EndExport();
                return;
            }

            try
            {
                var dataNode = _entManager.System<HumanoidAppearanceSystem>().ToDataNode(Profile);
                await using var writer = new StreamWriter(file.Value.fileStream);
                dataNode.Write(writer);
            }
            catch (Exception exc)
            {
                _sawmill.Error($"Error when exporting profile\n{exc.StackTrace}");
            }
            finally
            {
                EndExport();
                await file.Value.fileStream.DisposeAsync();
            }
        }

        private void StartExport()
        {
            _exporting = true;
            ImportButton.Disabled = true;
            ExportButton.Disabled = true;
        }

        private void EndExport()
        {
            _exporting = false;
            ImportButton.Disabled = false;
            ExportButton.Disabled = false;
        }

        /// <summary>
        /// Calculates the total stat bonus from free learned skills (spells) for a given stat.
        /// Returns 0 if no spells grant bonus to that stat.
        /// </summary>
        private int OE14GetSpellBonusForStat(string statName)
        {
            if (Profile == null)
                return 0;

            // Get the species prototype to find free learned skills
            if (!_prototypeManager.TryIndex<SpeciesPrototype>(Profile.Species, out var specProto))
                return 0;

            if (!_prototypeManager.TryIndex<EntityPrototype>(specProto.Prototype, out var entProto))
                return 0;

            // Look for OE14SkillStorage component which lists free skills
            if (!entProto.TryGetComponent<Content.Shared._OE14.Skill.Components.OE14SkillStorageComponent>(
                out var skillStorageComp, _entManager.ComponentFactory))
                return 0;

            if (skillStorageComp == null || skillStorageComp.FreeLearnedSkills == null)
                return 0;

            var freeSkillsList = skillStorageComp.FreeLearnedSkills;

            int totalBonus = 0;

            // For each free skill, check if it has OE14AddStatBonus effect for our stat
            foreach (var skillId in freeSkillsList)
            {
                if (!_prototypeManager.TryIndex(skillId, out OE14SkillPrototype? skillProto))
                    continue;

                if (skillProto == null)
                    continue;

                // Check effects for OE14AddStatBonus
                if (skillProto.Effects != null)
                {
                    foreach (var effect in skillProto.Effects)
                    {
                        if (effect is Content.Shared._OE14.Skill.Effects.OE14AddStatBonus statBonus &&
                            statBonus.Stat.Equals(statName, StringComparison.OrdinalIgnoreCase))
                        {
                            totalBonus += 1; // Each T1/T2 gives +1
                        }
                    }
                }
            }

            return totalBonus;
        }

        /// <summary>
        /// Creates a two-layer bar inside <paramref name="slot"/>:
        /// - Background: very dark version of the stat color
        /// - Foreground: full stat color, width tracks value/10
        /// Returns a setter callback to update the fill ratio (0-1).
        /// </summary>
        private static void OE14BuildStatBar(BoxContainer slot, Color color, out Action<float> setter)
        {
            var dark = new Color(color.R * 0.18f, color.G * 0.18f, color.B * 0.18f);

            var layout = new LayoutContainer { HorizontalExpand = true, MinHeight = 10 };

            var bg = new PanelContainer { PanelOverride = new StyleBoxFlat { BackgroundColor = dark } };
            LayoutContainer.SetAnchorRight(bg, 1f);
            LayoutContainer.SetAnchorBottom(bg, 1f);
            layout.AddChild(bg);

            var fg = new PanelContainer { PanelOverride = new StyleBoxFlat { BackgroundColor = color } };
            LayoutContainer.SetAnchorRight(fg, 0f);
            LayoutContainer.SetAnchorBottom(fg, 1f);
            layout.AddChild(fg);

            slot.AddChild(layout);

            setter = ratio =>
            {
                ratio = Math.Clamp(ratio, 0f, 1f);
                LayoutContainer.SetAnchorRight(fg, ratio);
            };
        }

        /// <summary>
        /// Populates a BoxContainer with modifier labels.
        /// Shows both "Spend" and "Spell" modifiers.
        /// </summary>
        private static void OE14PopulateModifierContainer(BoxContainer container, int spendBonus, int spellBonus)
        {
            container.RemoveAllChildren();

            bool first = true;

            if (spellBonus != 0)
            {
                if (!first)
                {
                    var separator = new Label { Text = ", ", FontColorOverride = Color.FromHex("#999999") };
                    container.AddChild(separator);
                }

                var sign = spellBonus > 0 ? "+" : "";
                var label = new Label
                {
                    Text = $"{sign}{spellBonus} SPEL",
                    FontColorOverride = Color.FromHex("#d946ef")
                };
                container.AddChild(label);
                first = false;
            }

            if (spendBonus != 0)
            {
                if (!first)
                {
                    var separator = new Label { Text = ", ", FontColorOverride = Color.FromHex("#999999") };
                    container.AddChild(separator);
                }

                var sign = spendBonus > 0 ? "+" : "";
                var label = new Label
                {
                    Text = $"{sign}{spendBonus} SPND",
                    FontColorOverride = Color.FromHex("#f0c050")
                };
                container.AddChild(label);
            }
        }
