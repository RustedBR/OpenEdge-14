        /// <summary>
        /// Refreshes the flavor text editor status.
        /// </summary>
        public void RefreshFlavorText()
        {
            if (_allowFlavorText)
            {
                if (_flavorText != null)
                    return;

                _flavorText = new FlavorText.FlavorText();
                TabContainer.AddChild(_flavorText);
                TabContainer.SetTabTitle(TabContainer.ChildCount - 1, Loc.GetString("humanoid-profile-editor-flavortext-tab"));
                _flavorTextEdit = _flavorText.CFlavorTextInput;

                _flavorText.OnFlavorTextChanged += OnFlavorTextChange;
            }
            else
            {
                if (_flavorText == null)
                    return;

                TabContainer.RemoveChild(_flavorText);
                _flavorText.OnFlavorTextChanged -= OnFlavorTextChange;
                _flavorText.Dispose();
                _flavorTextEdit?.Dispose();
                _flavorTextEdit = null;
                _flavorText = null;
            }
        }

        /// <summary>
        /// Refreshes traits selector
        /// </summary>
        public void RefreshTraits()
        {
            TraitsList.DisposeAllChildren();

            var traits = _prototypeManager.EnumeratePrototypes<TraitPrototype>().OrderBy(t => Loc.GetString(t.Name)).ToList();
            TabContainer.SetTabTitle(3, Loc.GetString("humanoid-profile-editor-traits-tab"));

            if (traits.Count < 1)
            {
                TraitsList.AddChild(new Label
                {
                    Text = Loc.GetString("humanoid-profile-editor-no-traits"),
                    FontColorOverride = Color.Gray,
                });
                return;
            }

            // Setup model
            Dictionary<string, List<string>> traitGroups = new();
            List<string> defaultTraits = new();
            traitGroups.Add(TraitCategoryPrototype.Default, defaultTraits);

            foreach (var trait in traits)
            {
                if (trait.Category == null)
                {
                    defaultTraits.Add(trait.ID);
                    continue;
                }

                if (!_prototypeManager.HasIndex(trait.Category))
                    continue;

                var group = traitGroups.GetOrNew(trait.Category);
                group.Add(trait.ID);
            }

            // Create UI view from model
            foreach (var (categoryId, categoryTraits) in traitGroups)
            {
                TraitCategoryPrototype? category = null;

                if (categoryId != TraitCategoryPrototype.Default)
                {
                    category = _prototypeManager.Index<TraitCategoryPrototype>(categoryId);
                    // Label
                    TraitsList.AddChild(new Label
                    {
                        Text = Loc.GetString(category.Name),
                        Margin = new Thickness(0, 10, 0, 0),
                        StyleClasses = { StyleBase.StyleClassLabelHeading },
                    });
                }

                List<TraitPreferenceSelector?> selectors = new();
                var selectionCount = 0;

                foreach (var traitProto in categoryTraits)
                {
                    var trait = _prototypeManager.Index<TraitPrototype>(traitProto);
                    var selector = new TraitPreferenceSelector(trait);

                    selector.Preference = Profile?.TraitPreferences.Contains(trait.ID) == true;
                    if (selector.Preference)
                        selectionCount += trait.Cost;

                    selector.PreferenceChanged += preference =>
                    {
                        if (preference)
                        {
                            Profile = Profile?.WithTraitPreference(trait.ID, _prototypeManager);
                        }
                        else
                        {
                            Profile = Profile?.WithoutTraitPreference(trait.ID, _prototypeManager);
                        }

                        SetDirty();
                        RefreshTraits(); // If too many traits are selected, they will be reset to the real value.
                    };
                    selectors.Add(selector);
                }

                // Selection counter
                if (category is { MaxTraitPoints: >= 0 })
                {
                    TraitsList.AddChild(new Label
                    {
                        Text = Loc.GetString("humanoid-profile-editor-trait-count-hint", ("current", selectionCount) ,("max", category.MaxTraitPoints)),
                        FontColorOverride = Color.Gray
                    });
                }

                foreach (var selector in selectors)
                {
                    if (selector == null)
                        continue;

                    if (category is { MaxTraitPoints: >= 0 } &&
                        selector.Cost + selectionCount > category.MaxTraitPoints)
                    {
                        selector.Checkbox.Label.FontColorOverride = Color.Red;
                    }

                    TraitsList.AddChild(selector);
                }
            }
        }

        // OE14: Total stat points available at character creation.
        private const int OE14MaxStatValue = 10;
        private const int OE14CreationPointsDefault = 3;
        private const int OE14CreationPointsHuman = 5;

        /// <summary>
        /// Reads the base stats for the current species from its entity prototype.
        /// Returns neutral (5,5,5,5) if not found.
        /// </summary>
        private (int str, int vit, int dex, int intel) OE14GetBaseStats()
        {
            if (Profile == null)
                return (5, 5, 5, 5);

            if (!_prototypeManager.TryIndex<SpeciesPrototype>(Profile.Species, out var specProto))
                return (5, 5, 5, 5);

            if (!_prototypeManager.TryIndex<EntityPrototype>(specProto.Prototype, out var entProto))
                return (5, 5, 5, 5);

            if (!entProto.TryGetComponent<OE14CharacterStatsComponent>(out var baseStats, _entManager.ComponentFactory))
                return (5, 5, 5, 5);

            return (baseStats.Strength, baseStats.Vitality, baseStats.Dexterity, baseStats.Intelligence);
        }

        /// <summary>
        /// Returns the number of creation points available based on species.
        /// Humans get 5, all others get 3.
        /// </summary>
        private int OE14GetCreationPoints()
        {
            if (Profile == null)
                return OE14CreationPointsDefault;

            return Profile.Species == "OE14Human"
                ? OE14CreationPointsHuman
                : OE14CreationPointsDefault;
        }

        /// <summary>
        /// Refreshes the OE14 stat distribution UI from the current profile.
        /// </summary>
        public void RefreshOE14Stats()
        {
            if (Profile == null)
                return;

            var (baseStr, baseVit, baseDex, baseInt) = OE14GetBaseStats();

            var bonusStr = Profile.OE14StatBonusStrength;
            var bonusVit = Profile.OE14StatBonusVitality;
            var bonusDex = Profile.OE14StatBonusDexterity;
            var bonusInt = Profile.OE14StatBonusIntelligence;

            // Get spell bonuses from free learned skills
            var spellBonusStr = OE14GetSpellBonusForStat("strength");
            var spellBonusVit = OE14GetSpellBonusForStat("vitality");
            var spellBonusDex = OE14GetSpellBonusForStat("dexterity");
            var spellBonusInt = OE14GetSpellBonusForStat("intelligence");

            // Final stats include base + spend bonus + spell bonus
            var finalStr = Math.Min(baseStr + bonusStr + spellBonusStr, OE14MaxStatValue);
            var finalVit = Math.Min(baseVit + bonusVit + spellBonusVit, OE14MaxStatValue);
            var finalDex = Math.Min(baseDex + bonusDex + spellBonusDex, OE14MaxStatValue);
            var finalInt = Math.Min(baseInt + bonusInt + spellBonusInt, OE14MaxStatValue);

            var spent = bonusStr + bonusVit + bonusDex + bonusInt;
            var creationPoints = OE14GetCreationPoints();
            var available = creationPoints - spent;

            OE14AvailablePointsLabel.Text = Loc.GetString("oe14-stats-tab-points-value", ("value", available));

            // Base stats labels
            OE14StrengthBaseLabel.Text = baseStr.ToString();
            OE14VitalityBaseLabel.Text = baseVit.ToString();
            OE14DexterityBaseLabel.Text = baseDex.ToString();
            OE14IntelligenceBaseLabel.Text = baseInt.ToString();

            // Current (final) stats labels
            OE14StrengthValue.Text = finalStr.ToString();
            OE14VitalityValue.Text = finalVit.ToString();
            OE14DexterityValue.Text = finalDex.ToString();
            OE14IntelligenceValue.Text = finalInt.ToString();

            // Update bar fills (ratio 0-1 over MaxStatValue)
            var max = (float)OE14MaxStatValue;
            _strBarSetter?.Invoke(finalStr / max);
            _vitBarSetter?.Invoke(finalVit / max);
            _dexBarSetter?.Invoke(finalDex / max);
            _intBarSetter?.Invoke(finalInt / max);

            // Populate modifier containers (Spend mods + Spell mods from free skills)
            OE14PopulateModifierContainer(OE14StrengthModContainer, bonusStr, spellBonusStr);
            OE14PopulateModifierContainer(OE14VitalityModContainer, bonusVit, spellBonusVit);
            OE14PopulateModifierContainer(OE14DexterityModContainer, bonusDex, spellBonusDex);
            OE14PopulateModifierContainer(OE14IntelligenceModContainer, bonusInt, spellBonusInt);

            // + buttons: disabled if no budget left OR stat already at max (including spell bonuses)
            OE14StrPlus.Disabled = available <= 0 || (baseStr + bonusStr + spellBonusStr) >= OE14MaxStatValue;
            OE14VitPlus.Disabled = available <= 0 || (baseVit + bonusVit + spellBonusVit) >= OE14MaxStatValue;
            OE14DexPlus.Disabled = available <= 0 || (baseDex + bonusDex + spellBonusDex) >= OE14MaxStatValue;
            OE14IntPlus.Disabled = available <= 0 || (baseInt + bonusInt + spellBonusInt) >= OE14MaxStatValue;

            // - buttons: disabled if bonus is 0
            OE14StrMinus.Disabled = bonusStr <= 0;
            OE14VitMinus.Disabled = bonusVit <= 0;
            OE14DexMinus.Disabled = bonusDex <= 0;
            OE14IntMinus.Disabled = bonusInt <= 0;

            // Derived values preview
            var healthBonus = (finalVit - 5) * 12.5f;
            var critHp = 100f + healthBonus;
            var deadHp = critHp * 2f;
            var stamina = 100f * (1f + (finalDex - 5) * 0.1875f);
            var mana = 100f + (finalInt - 5) * 25f;
            var damage = finalStr >= 5
                ? 1f + (finalStr - 5) * 0.10f
                : 1f + (finalStr - 5) * 0.125f;

            var maxMana = Math.Max(0f, mana);
            var regenPerTick = maxMana / 100f;
            var staminaSlow = stamina * 0.25f;
            var staminaKnockdown = stamina;

            OE14DerivedHP.Text = critHp.ToString("0");
            OE14DerivedHPThresholds.Text = $"- {Math.Abs((int)critHp)} crit / - {Math.Abs((int)deadHp)} dead";

            OE14DerivedStamina.Text = $"{stamina:0} / {stamina:0}";
            OE14DerivedStaminaSlow.Text = $"slow at - {staminaSlow:0} / Knockdown at - {staminaKnockdown:0}";

            OE14DerivedMana.Text = maxMana.ToString("0");
            OE14DerivedManaRegen.Text = regenPerTick.ToString("0.##") + "/3s";

            OE14DerivedDamage.Text = damage.ToString("0.00") + "x";
        }

        private void OE14SpendStatPoint(string stat, int delta)
        {
            if (Profile == null)
                return;

            var (baseStr, baseVit, baseDex, baseInt) = OE14GetBaseStats();

            var bonusStr = Profile.OE14StatBonusStrength;
            var bonusVit = Profile.OE14StatBonusVitality;
            var bonusDex = Profile.OE14StatBonusDexterity;
            var bonusInt = Profile.OE14StatBonusIntelligence;
            var spent = bonusStr + bonusVit + bonusDex + bonusInt;
            var creationPoints = OE14GetCreationPoints();
            var available = creationPoints - spent;

            var (currentBonus, baseVal) = stat switch
            {
                "strength" => (bonusStr, baseStr),
                "vitality" => (bonusVit, baseVit),
                "dexterity" => (bonusDex, baseDex),
                "intelligence" => (bonusInt, baseInt),
                _ => (0, 5)
            };

            if (delta > 0 && available <= 0)
                return;
            if (delta > 0 && baseVal + currentBonus >= OE14MaxStatValue)
                return;
            if (delta < 0 && currentBonus <= 0)
                return;

            Profile = Profile.WithOE14StatBonus(stat, currentBonus + delta);
            SetDirty();
            RefreshOE14Stats();
        }

